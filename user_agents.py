# 添加一些可能有用的函数
def get_random_user_agent():
    """获取随机User-Agent"""
    return random.choice(USER_AGENTS)

def get_windows_user_agent():
    """获取Windows平台的User-Agent"""
    return random.choice([ua for ua in USER_AGENTS if "Windows" in ua])

def get_mac_user_agent():
    """获取Mac平台的User-Agent"""
    return random.choice([ua for ua in USER_AGENTS if "Macintosh" in ua])

def get_linux_user_agent():
    """获取Linux平台的User-Agent"""
    return random.choice([ua for ua in USER_AGENTS if "Linux" in ua and "Android" not in ua])

def get_android_user_agent():
    """获取Android平台的User-Agent"""
    return random.choice([ua for ua in USER_AGENTS if "Android" in ua])

def get_chrome_user_agent():
    """获取Chrome浏览器的User-Agent(不含派生浏览器)"""
    return random.choice([ua for ua in USER_AGENTS if "Chrome" in ua and all(x not in ua for x in ["Edg", "Brave", "OPR", "Vivaldi", "YaBrowser"])])

def get_edge_user_agent():
    """获取Edge浏览器的User-Agent"""
    return random.choice([ua for ua in USER_AGENTS if "Edg" in ua])

